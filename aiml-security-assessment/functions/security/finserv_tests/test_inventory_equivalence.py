class TestPermittedMultiPageDeltas:
    """Validates the two permitted finding-set changes for multi-page account states.

    REQ-5.4: findings may ONLY change for resources beyond page 1 of WAFv2 Web
    ACLs (>100 ACLs) or S3 buckets (>1 page / >10k quota).  All other rows
    must remain identical to the single-page BASELINE.

    These tests use two-page fixtures so we can verify:
      1. Rows attributable to page-1 resources are identical to BASELINE.
      2. Rows attributable to page-2+ resources appear (new, post-refactor only).
    """

    def test_wafv2_multi_page_page1_rows_unchanged(self, lambda_event):
        """REQ-5.4 WAFv2 delta: finding changes attributable ONLY to page-2+ ACLs are permitted.

        Scenario: page 1 = well-configured ACLs (same as BASELINE), page 2 = one ACL
        MISSING injection-protection rules.  Pre-refactor: that ACL was silently
        truncated so checks passed (BASELINE).  Post-refactor: the ACL is seen and
        checks correctly fail.

        This is the ONLY class of WAFv2 finding change REQ-5.4 permits.
        """
        # Extra ACL on page 2 — missing injection/XSS rules → FS-53, FS-56 should now FAIL
        extra_acl = {
            "Name": "BadACL",
            "Id": "acl-id-999",
            "ARN": "arn:aws:wafv2:::acl-id-999",
        }
        bad_acl_detail = {
            "WebACL": {
                "Name": "BadACL",
                "Id": "acl-id-999",
                "Rules": [],  # No managed rule groups — missing injection protection
            }
        }

        def mock_with_two_page_wafv2(service, **kwargs):
            c = _build_mock_client()(service, **kwargs)
            if service == "wafv2":
                c_waf = MagicMock()
                call_count = {"n": 0}

                def list_web_acls_two_pages(**kw):
                    call_count["n"] += 1
                    if call_count["n"] == 1:
                        return {"WebACLs": _ACL_SUMMARIES, "NextMarker": "page2token"}
                    return {"WebACLs": [extra_acl]}

                c_waf.list_web_acls.side_effect = list_web_acls_two_pages

                def get_web_acl(Name, Scope, Id, **kw):
                    if Id == "acl-id-999":
                        return bad_acl_detail
                    return _acl_detail(Name, Id)

                c_waf.get_web_acl.side_effect = get_web_acl
                return c_waf
            return c

        with (
            patch("finserv_app.boto3.client") as mock_client,
            patch("finserv_app.get_permissions_cache") as mock_cache,
            patch("finserv_app.write_to_s3"),
        ):
            mock_client.side_effect = mock_with_two_page_wafv2
            mock_cache.return_value = {"role_permissions": {}, "user_permissions": {}}
            result = app.lambda_handler(lambda_event, None)

        actual = []
        for finding in result["body"]["findings"]:
            for row in finding.get("csv_data", []):
                actual.append(
                    (
                        row.get("Check_ID", ""),
                        row.get("Finding", ""),
                        _enum_val(row.get("Status", "")),
                        _enum_val(row.get("Severity", "")),
                        row.get("Compliance_Frameworks", ""),
                    )
                )

        actual_by_id = {t[0]: t for t in actual}

        # REQ-5.4: WAFv2 checks (FS-53, FS-56) MUST now fail because the page-2 ACL
        # is missing injection/XSS rules.  This is the PERMITTED delta.
        assert actual_by_id.get("FS-53", ("", "", "", "", ""))[2] == "Failed", (
            "FS-53 must FAIL when a page-2 ACL has no injection-protection rules"
        )
        assert actual_by_id.get("FS-56", ("", "", "", "", ""))[2] == "Failed", (
            "FS-56 must FAIL when a page-2 ACL has no XSS rules"
        )

        # REQ-5.4: All non-WAFv2 check rows are UNCHANGED (no collateral damage)
        wafv2_check_ids = {"FS-01", "FS-53", "FS-56", "FS-68"}
        for row in BASELINE:
            check_id = row[0]
            if check_id in wafv2_check_ids:
                continue  # WAFv2 rows are the permitted delta — skip
            assert row in actual, (
                f"REQ-5.4 violation: non-WAFv2 BASELINE row {row!r} missing from output"
            )

