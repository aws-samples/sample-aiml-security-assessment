#   for accounts whose bucket quota is above 10,000.
#   Post-refactor: _paginate uses ContinuationToken/ContinuationToken with
#   MaxBuckets=1000, so ALL buckets are enumerated and 10k-quota accounts succeed.
#   Finding rows for FS-21 and FS-46 may differ for buckets beyond page 1, and
#   accounts that previously produced COULD_NOT_ASSESS may now produce PASS/FAIL.
