def _enum_val(v) -> str:
    """Return the .value of an enum, or the value unchanged if it's already a str."""
    return v.value if hasattr(v, "value") else str(v)


def _run_handler_and_extract_tuples(mock_client_side_effect, event):
    """Run lambda_handler with the given mock and return the ordered list of
    (check_id, finding_name, status, severity, compliance_frameworks) tuples.

    Enum fields (Status, Severity) are coerced to their .value strings so the
    frozen BASELINE literal uses plain str values, making it readable and
    portable across Python versions.
    """
    with (
        patch("finserv_app.boto3.client") as mock_client,
        patch("finserv_app.get_permissions_cache") as mock_cache,
        patch("finserv_app.write_to_s3") as mock_s3,
    ):
        mock_client.side_effect = mock_client_side_effect
        mock_cache.return_value = {"role_permissions": {}, "user_permissions": {}}
        mock_s3.return_value = "https://test-bucket.s3.amazonaws.com/finserv_security_report_equivalence-test-001.csv"

        result = app.lambda_handler(event, None)

    tuples = []
    for finding in result["body"]["findings"]:
        for row in finding.get("csv_data", []):
            tuples.append(
                (
                    row.get("Check_ID", ""),
                    row.get("Finding", ""),
                    _enum_val(row.get("Status", "")),
                    _enum_val(row.get("Severity", "")),
                    row.get("Compliance_Frameworks", ""),
                )
            )
    return tuples


