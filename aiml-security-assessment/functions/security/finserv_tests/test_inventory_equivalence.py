# finding-set changes — both attributable to pagination-correctness fixes:
#
# PERMITTED DELTA 1 — WAFv2 Web ACLs (REQ-2.2):
#   Pre-refactor: wafv2.list_web_acls(Scope="REGIONAL") was called UNPAGINATED,
#   silently truncating at one page (≤100 ACLs).  Accounts with >100 REGIONAL
#   Web ACLs would have their ACL set incorrectly truncated.
#   Post-refactor: _paginate uses NextMarker/NextMarker token convention, so
#   ALL ACLs are returned.  Any finding rows attributable to ACLs beyond page 1
